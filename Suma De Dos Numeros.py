# Codigo en lenguaje Python que solicite al usuario que ingrese dos numeros, los sume y muestre el resultado

# Solicitar al usuario que ingrese dos numeros
print("SUMA DE DOS NUMEROS\n\n")

# Ingresar el primer numero
num1 = float(input("Ingrese El Primer Numero: \n"))

print("\n")

# Ingresar el segundo numero
num2 = float(input("Ingrese El Segundo Numero: \n"))

# Sumar los dos numeros
resultado = num1 + num2

print("\n\n")

# Mostrar el resultado
print(f"La Suma de {num1:.2f} y {num2:.2f} es: {resultado:.2f}\n\n\n\n\n")

